class CandidatesController < ApplicationController
  before_filter :require_admin, except: [:index, :show]
  before_action :set_candidate, only: [:show, :edit, :update, :destroy]

  # GET /candidates
  # GET /candidates.json
  def index
    @candidates = Candidate.all.includes(:person).order('people.last_name').order('people.first_name')
  end

  # GET /candidates/1
  # GET /candidates/1.json
  def show
    @videos = @candidate.statements.approved
  end

  # GET /candidates/new
  def new
    @candidate = Candidate.new
  end

  # GET /candidates/1/edit
  def edit
  end

  # POST /candidates
  # POST /candidates.json
  def create
    issues = {}
    if params[:issues].present? 
      params[:issues].each do |issue|
        issues[issue] = params["scaling_#{issue}"]
      end
    end
    @candidate = Candidate.new(candidate_params.merge!(scaling: issues))

    respond_to do |format|
      if @candidate.save
        format.html { redirect_to @candidate, notice: 'Candidate was successfully created.' }
        format.json { render :show, status: :created, location: @candidate }
      else
        format.html { render :new }
        format.json { render json: @candidate.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /candidates/1
  # PATCH/PUT /candidates/1.json
  def update
    respond_to do |format|
      issues = {}
      if params[:issues].present? 
        params[:issues].each do |issue|
          issues[issue] = params["scaling_#{issue}"]
        end
      end
      if @candidate.update(candidate_params.merge!(scaling: issues))
        format.html { redirect_to @candidate, notice: 'Candidate was successfully updated.' }
        format.json { render :show, status: :ok, location: @candidate }
      else
        format.html { render :edit }
        format.json { render json: @candidate.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /candidates/1
  # DELETE /candidates/1.json
  def destroy
    @candidate.destroy
    respond_to do |format|
      format.html { redirect_to candidates_url, notice: 'Candidate was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_candidate
      @candidate = Candidate.includes(:campaign).find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def candidate_params
      params.require(:candidate).permit(:rwu_id, 
                                        :person_id, 
                                        :office_id, 
                                        :position, 
                                        :district,
                                        :party,
                                        :status,
                                        :scaling,
                                        :description)
    end
end
